local s = require("bitops")
assert(s.arshift(0x80000000, 32) == 0xFFFFFFFF, "arshift 32")
assert(s.arshift(0x00000001, -31) == 0x80000000, "arshift -31")
assert(s.arshift(0x00000001, -32) == 0, "arshift -32")
assert(s.rshift(0x80000000, 32) == 0, "rshift 32")
assert(s.rshift(0x00000001, -31) == 0x80000000, "rshift -31")
assert(s.rshift(0x00000001, -32) == 0, "rshift -32")
assert(s.lshift(0x00000001, 32) == 0, "lshift 32")
assert(s.lshift(0x80000000, -32) == 0, "lshift -32")
